import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "../..");
const inputPath = process.env.LEGACY_GAME_DATA
  ? resolve(process.env.LEGACY_GAME_DATA)
  : resolve(root, "web/public/ideal-item-data.json");
const input = JSON.parse(readFileSync(inputPath, "utf8"));
const outputDir = resolve(root, "cdk/packages/game-data/generated");
mkdirSync(outputDir, { recursive: true });

const q = value => JSON.stringify(value);
const className = value => value
  .replace(/[^a-zA-Z0-9]+/g, " ")
  .trim()
  .split(/\s+/)
  .map(part => part[0]?.toUpperCase() + part.slice(1))
  .join("");

const classesByBase = new Map();
for (const itemClass of input.classes) {
  for (const baseId of itemClass.baseIds) {
    const list = classesByBase.get(baseId) ?? [];
    list.push(itemClass);
    classesByBase.set(baseId, list);
  }
}

const modifiers = new Map();
for (const mods of Object.values(input.mods)) {
  for (const mod of mods) {
    const metadata = {
      id: mod.modId,
      name: mod.name,
      affix: mod.affix,
      groups: mod.modgroups ?? [],
      tags: mod.tags ?? [],
      ...(mod.statId ? { statId: mod.statId } : {}),
    };
    const existing = modifiers.get(mod.modId);
    if (existing && q(existing) !== q(metadata)) {
      throw new Error(`Modifier metadata differs by equipment type: ${mod.modId}`);
    }
    modifiers.set(mod.modId, metadata);
  }
}

const modifierLines = [...modifiers.values()]
  .sort((a, b) => a.id.localeCompare(b.id))
  .map(mod => `  ${q(mod.id)}: new ModifierDefinition(${q(mod)}),`);
writeFileSync(
  resolve(outputDir, "modifiers.ts"),
  `// Generated by scripts/bootstrap-game-data.mjs. This committed file is game-data source code.\n` +
  `import { ModifierDefinition } from "../model";\n\n` +
  `export const MODIFIERS: Record<string, ModifierDefinition> = {\n${modifierLines.join("\n")}\n};\n`,
);

writeFileSync(
  resolve(outputDir, "item-classes.ts"),
  `// Generated by scripts/bootstrap-game-data.mjs. These are UI/domain groupings over equipment base types.\n` +
  `export const ITEM_CLASSES = ${JSON.stringify(input.classes, null, 2)} as const;\n`,
);

const duplicateLabels = new Map();
for (const [baseId, itemClasses] of classesByBase) {
  const label = itemClasses[0]?.label ?? `Base ${baseId}`;
  duplicateLabels.set(label, (duplicateLabels.get(label) ?? 0) + 1);
}

const equipmentClasses = [];
const instances = [];
for (const [baseId, mods] of Object.entries(input.mods).sort(([a], [b]) => Number(a) - Number(b))) {
  const itemClasses = classesByBase.get(baseId) ?? [];
  const label = itemClasses[0]?.label ?? `Base ${baseId}`;
  const baseClass = itemClasses.some(c => c.id.startsWith("weapon."))
    ? "WeaponType"
    : itemClasses.some(c => c.id.startsWith("accessory."))
      ? "AccessoryType"
      : "ArmourType";
  const name = `${className(label)}${duplicateLabels.get(label) > 1 ? `Base${baseId}` : ""}`;
  const modLines = mods.map(mod => {
    const tiers = mod.tiers.map(tier => ({
      tier: tier.tier,
      requiredLevel: tier.ilvl,
      weight: tier.weight,
      values: tier.values ?? [],
    }));
    return `      equipmentMod(MODIFIERS[${q(mod.modId)}], ${q(tiers)}),`;
  });
  equipmentClasses.push(
    `export class ${name} extends ${baseClass} {\n` +
    `  constructor() {\n` +
    `    super({\n` +
    `      id: ${q(baseId)},\n` +
    `      label: ${q(label)},\n` +
    `      itemClassIds: ${q(itemClasses.map(c => c.id))},\n` +
    `      baseItems: ${q(input.baseItems[baseId] ?? [])},\n` +
    `      mods: [\n${modLines.join("\n")}\n      ],\n` +
    `    });\n` +
    `  }\n` +
    `}`,
  );
  instances.push(`  new ${name}(),`);
}

writeFileSync(
  resolve(outputDir, "equipment-types.ts"),
  `// Generated by scripts/bootstrap-game-data.mjs. Equipment classes explicitly own their mod pools.\n` +
  `import { AccessoryType, ArmourType, WeaponType, equipmentMod } from "../model";\n` +
  `import { MODIFIERS } from "./modifiers";\n\n` +
  `${equipmentClasses.join("\n\n")}\n\n` +
  `export const EQUIPMENT_TYPES = [\n${instances.join("\n")}\n] as const;\n`,
);

console.log(`Wrote ${modifiers.size} modifiers, ${instances.length} equipment classes, and ${input.classes.length} item classes to ${outputDir}`);
